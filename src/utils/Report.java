package utils;

public class Report {
    private String title;
    private String description;
    public void setTitle(String statisticalReport) {
        this.title = statisticalReport;
    }

    public void setDescription(String s) {
        this.description = s;
    }

    public String getTitle() {
        return title;

    }

    public String getDescription() {
        return description;
    }
}
